def main():
    layer = {
        '1': 0, 'Q': 0, 'A': 0, 'Z': 0,
        '2': 1, 'W': 1, 'S': 1, 'X': 1,
        '3': 2, 'E': 2, 'D': 2, 'C': 2,
        '4': 3, 'R': 3, 'F': 3, 'V': 3,
        '5': 3, 'T': 3, 'G': 3, 'B': 3,
        '6': 4, 'Y': 4, 'H': 4, 'N': 4,
        '7': 4, 'U': 4, 'J': 4, 'M': 4,
        '8': 5, 'I': 5, 'K': 5, ',': 5,
        '9': 6, 'O': 6, 'L': 6, '.': 6,
        '0': 7, 'P': 7, ';': 7, '/': 7,
        '-': 7, '[': 7, "'": 7,
        '=': 7, ']': 7
    }
    cnt = [0] * 8

    text = input()
    for c in text:
        cnt[layer[c]] += 1
    print(*cnt, sep='\n')


